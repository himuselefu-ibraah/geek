#arithmetic operators

x=40
y=32
print(x + y)
print(x - y)
print(x * y)
print(x / y)
print(x % y) #modulus returns remainder after division

#comparison operators

print(x<y)
print(x>y)
print(x<=y)
print(x>=y)
